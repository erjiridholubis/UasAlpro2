class Customer {

  int id;
  String nama_pelanggan;
  String alamat_pelanggan;
  String no_hp;

  public Customer(int id, String nama_pelanggan, String alamat_pelanggan, String no_hp) {
    this.id = id;
    this.nama_pelanggan = nama_pelanggan;
    this.alamat_pelanggan = alamat_pelanggan;
    this.no_hp = no_hp;
  }

}
